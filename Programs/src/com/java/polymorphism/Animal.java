package com.java.polymorphism;

public class Animal {
	String hungry;
	String sound;
	String jump;
	public String getHungry() {
		return hungry;
	}
	public void setHungry(String hungry) {
		this.hungry = hungry;
	}
	public String getSound() {
		return sound;
	}
	public void setSound(String sound) {
		this.sound = sound;
	}
	public String getJump() {
		return jump;
	}
	public void setJump(String jump) {
		this.jump = jump;
	}
	

}
